pub fn text(language_code: &str, key: &str) -> &'static str {
    let lang = normalize_language(language_code);
    lookup_with_fallback(lang, key).unwrap_or("<missing-l10n-key>")
}

static CURRENT_LANGUAGE: LazyLock<RwLock<String>> = LazyLock::new(|| RwLock::new("en-US".to_string()));

pub fn set_current_language(language_code: &str) {
    let normalized = normalize_language(language_code).to_string();
    if let Ok(mut guard) = CURRENT_LANGUAGE.write() {
        *guard = normalized;
    }
}

pub fn current_language() -> String {
    if let Ok(guard) = CURRENT_LANGUAGE.read() {
        return guard.clone();
    }
    "en-US".to_string()
}

pub fn tr(source: &'static str) -> &'static str {
    let lang = current_language();
    tr_in(&lang, source)
}

