fn lookup_with_fallback(lang: &str, key: &str) -> Option<&'static str> {
    lookup(lang, key)
        .or_else(|| match lang {
            "zh-Hans" => lookup("zh-Hant", key),
            "zh-Hant" => lookup("zh-Hans", key),
            "lzh" => lookup("zh-Hans", key).or_else(|| lookup("zh-Hant", key)),
            "pt-PT" => lookup("pt-BR", key),
            _ => None,
        })
        .or_else(|| lookup("en-US", key))
}

